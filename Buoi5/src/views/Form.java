    //Phương thức loadToCombo
    public void loadToCombo() {
        ArrayList<String> list = ser.getNganh();
        for (String s : list) {
            cbb.addElement(s);
        }
    }

    //Phương thức loadTable
    public void LoadTable(ArrayList<SinhVien> li) {
        dtm.setRowCount(0);
        for (SinhVien s : li) {
            dtm.addRow(new Object[]{
                s.getMaSV(),
                s.getHoTen(),
                s.getDiemSV(),
                s.getNganhSV()
            });
        }
    }

    public void FillData(SinhVien sv) {
        txtMa.setText(sv.getMaSV());
        txtHoTen.setText(sv.getHoTen());
        txtDiem.setText(sv.getDiemSV() + "");
        cbbNganh.setSelectedItem(sv.getNganhSV());
    }

    public void clearForm() {
        txtMa.setText("");
        txtHoTen.setText("");
        txtDiem.setText("");
        cbbNganh.setSelectedIndex(0);
    }

    public SinhVien getDataForm() {
        String ma = txtMa.getText();
        String ten = txtHoTen.getText();
        String diem = txtDiem.getText();
        String nganh = cbbNganh.getSelectedItem().toString();
        float diemSV;

        if (ten.isEmpty() || diem.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu !!");
            return null;
        }

        try {
            diemSV = Float.parseFloat(diem);
            if (diemSV < 0 || diemSV > 10) {
                JOptionPane.showMessageDialog(this, "Điểm trong khoảng từ 0-10 !!");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số !!");
            return null;
        }

        SinhVien sv = new SinhVien(ma, ten, diemSV, nganh);
        return sv;
    }

