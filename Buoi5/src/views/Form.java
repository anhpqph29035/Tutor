    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tblSV.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để xóa");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String ma = tblSV.getValueAt(row, 0).toString();
        ser.deleteSV(ma);
        JOptionPane.showMessageDialog(this, "Xóa thành công !!");
        list = ser.getAllSV();
        LoadTable(list);
        clearForm();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        SinhVien sv = getDataForm();
        if (sv == null) {
            return;
        }
        ser.themSV(sv);
        list = ser.getAllSV();
        JOptionPane.showMessageDialog(this, "Thêm thành công !!");
        LoadTable(list);
        clearForm();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tblSV.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Chưa chọn dòng để sửa");
            return;
        }
        int check = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không ?");
        if (check != JOptionPane.YES_OPTION) {
            return;
        }
        String ma = list.get(row).getMaSV();
        SinhVien sv = getDataForm();
        if(sv==null){
           return;
        }
        ser.suaSV(sv, ma);
        list = ser.getAllSV();
         JOptionPane.showMessageDialog(this, "Sửa thành công !!");
        LoadTable(list);
        clearForm();
    }//GEN-LAST:event_btnSuaActionPerformed

