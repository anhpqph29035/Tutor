    public void LoadTable(ArrayList<SinhVien> li) {
        dtm.setRowCount(0);
        for (SinhVien s : li) {
            dtm.addRow(new Object[]{
                s.getMasV(),
                s.getHoten(),
                s.getDiemSV(),
                s.getNganhSv()
            });
        }
    }

    private void FillData(SinhVien sv) {
        txtMa.setText(sv.getMasV());
        txtDiem.setText(sv.getDiemSV() + "");
        txtNganh.setText(sv.getNganhSv());
        txtTen.setText(sv.getHoten());
    }

    public void clear() {
        txtDiem.setText("");
        txtMa.setText("");
        txtNganh.setText("");
        txtTen.setText("");
    }

    public SinhVien Validate() {
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String diem = txtDiem.getText();
        String nganh = txtNganh.getText();
        float diemSv;

        if (ma.isEmpty() || ten.isEmpty() || diem.isEmpty() || nganh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống dữ liệu");
            return null;
        }

        try {
            diemSv = Float.parseFloat(diem);
            if (diemSv < 0 || diemSv > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải là số >=0 và <=10");
                return null;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Điểm phải là số");
            return null;
        }
        SinhVien sv = new SinhVien(ma, ten, diemSv, nganh);
        return sv;
    }

